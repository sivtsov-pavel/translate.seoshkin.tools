-- Одноразовая чистка: у готовых уроков (status='done') в progress иногда оставался текст
-- от прошлой обработки («Перевожу упражнения...» и т.п.) — визуально уже не показывается
-- (фронт гейтит по status), но в БД мусорил. Обнуляем на будущее — новые прогоны сами
-- пишут свежий progress при завершении, стухшего текста больше взяться неоткуда.
UPDATE lessons SET progress = NULL WHERE status = 'done' AND progress IS NOT NULL;
